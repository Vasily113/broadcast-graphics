const unsigned kPrerollSize = 1;
const BMDPixelFormat kKeyerPixelFormat = bmdFormat8BitBGRA;
const unsigned kRampFrameCount = 30;		// Ramp over 30 frames

KeyerOutput::KeyerOutput() : QDialog(),
	m_outputEnabled(false),
	m_loopStills(false),
	m_scheduledPlaybackStopped(false)
{
	ui = new Ui::KeyerOutputDialog();
	ui->setupUi(this);

	m_layout = new QGridLayout(ui->previewContainer);
	m_layout->setContentsMargins(0, 0, 0, 0);

	try
	{
		m_previewView = new DeckLinkOpenGLWidget(this);
	}
	catch (const std::runtime_error& e)
	{
		QMessageBox::critical(this, "Failed to create OpenGL widget", e.what());
		return;
	}

	m_previewView->resize(ui->previewContainer->size());
	m_previewView->setSizePolicy(QSizePolicy::Expanding, QSizePolicy::Expanding);
	m_layout->addWidget(m_previewView, 0, 0, 0, 0);

	connect(ui->startButton, &QPushButton::clicked, this, &KeyerOutput::toggleStart);
	connect(ui->outputDevicePopup, QOverload<int>::of(&QComboBox::currentIndexChanged), this, &KeyerOutput::outputDeviceChanged);
	connect(ui->openFileButton, &QPushButton::clicked, this, &KeyerOutput::openImageFiles);
	connect(ui->alphaBlendSlider, &QSlider::valueChanged, this, &KeyerOutput::alphaSliderChanged);

	m_openFileDialog = new QFileDialog(this);
	m_openFileDialog->setOptions(QFileDialog::DontUseNativeDialog);
	m_openFileDialog->setNameFilter(tr("Images (*.png)"));
	m_openFileDialog->setViewMode(QFileDialog::Detail);
	m_openFileDialog->setDirectory(QDir::homePath());

	QFileInfo defaultImageFileInfo(":/bmd-1080-clean.png");
	m_fileNames << defaultImageFileInfo.absoluteFilePath();

	connect(m_openFileDialog, &QFileDialog::currentChanged, this, [&](const QString& str)
	{
		// Workaround to allow selection of either files or directory in file dialog
		QFileInfo fileInfo(str);

		if (fileInfo.isDir())
			m_openFileDialog->setFileMode(QFileDialog::Directory);
		else
			m_openFileDialog->setFileMode(QFileDialog::ExistingFiles);
	});

	displayFrameCount();
	enableInterface(false);
	enableStartButton(false);
	show();
}

void KeyerOutput::setup()
{
	//
	// Create and initialize DeckLink device discovery object
	try
	{
		m_deckLinkDiscovery = std::unique_ptr<DeckLinkDeviceDiscovery>(new DeckLinkDeviceDiscovery(this));
	}
	catch (const std::runtime_error& e)
	{
		QMessageBox::critical(this, "Failed to create DeckLink device discovery", e.what());
		return;
	}

	if (! m_deckLinkDiscovery->start())
	{
		QMessageBox::critical(this, "This application requires the DeckLink drivers installed.", "Please install the Blackmagic DeckLink drivers to use the features of this application.");
	}
}

void KeyerOutput::customEvent(QEvent *event)
{
	if (event->type() == kAddDeviceEvent)
	{
		com_ptr<IDeckLink> deckLink((dynamic_cast<DeckLinkDeviceDiscoveryEvent*>(event))->deckLink());
		addDevice(deckLink);
	}
	else if (event->type() == kRemoveDeviceEvent)
	{
		com_ptr<IDeckLink> deckLink((dynamic_cast<DeckLinkDeviceDiscoveryEvent*>(event))->deckLink());
		removeDevice(deckLink);
	}
}

void KeyerOutput::closeEvent(QCloseEvent *)
{
	disable();

	if (m_deckLinkDiscovery)
		m_deckLinkDiscovery->stop();
}

void KeyerOutput::enableInterface(bool enable)
{
	// Set the enable state of user interface elements
	for (auto& combobox : ui->groupBox->findChildren<QComboBox*>())
	{
		combobox->setEnabled(enable);
	}

	for (auto& button : ui->groupBox_3->findChildren<QAbstractButton*>())
	{
		button->setEnabled(enable);
	}

	for (auto& radioButton : ui->groupBox_2->findChildren<QRadioButton*>())
	{
		radioButton->setEnabled(enable);
	}
}

void KeyerOutput::enableStartButton(bool enable)
{
	ui->startButton->setEnabled(enable && ui->videoFormatPopup->count() != 0 && m_fileNames.size() != 0);
}

void KeyerOutput::displayFrameCount()
{
	ui->fileStatusLabel->setText(QString("%1 image%2 selected for playback.").arg(m_fileNames.size())
								.arg(m_fileNames.size() == 1 ? "" : "s"));
}

void KeyerOutput::openImageFiles()
{
	m_fileNames.clear();

	if (! m_openFileDialog->exec())
		return;

	QStringList selectedFiles = m_openFileDialog->selectedFiles();
	QFileInfo fileInfo(selectedFiles[0]);

	if (fileInfo.isDir())
	{
		// If directory has been selected, find all files with png extension
		QStringList filters("*.png");
		QDir fileInfoDir = fileInfo.absoluteFilePath();
		QFileInfoList fileInfoList = fileInfoDir.entryInfoList(filters, QDir::Files | QDir::NoDotAndDotDot);

		for (const auto& fileInfo : fileInfoList)
		{
			m_fileNames << fileInfo.absoluteFilePath();
		}
	}
	else
	{
		m_fileNames = selectedFiles;
	}

	displayFrameCount();
	enableStartButton(true);
}

void KeyerOutput::alphaSliderChanged(int value)
{
	m_selectedDevice->getKeyer()->SetLevel((uint8_t)value);
}

void KeyerOutput::toggleStart()
{
	if (! m_outputEnabled)
		enable();
	else
		disable();
}

void KeyerOutput::refreshDisplayModeMenu(void)
{
	// Populate the display mode menu with a list of display modes supported by the installed DeckLink card
	ui->videoFormatPopup->clear();

	m_selectedDevice->queryDisplayModes([this](com_ptr<IDeckLinkDisplayMode>& displayMode)
	{
		BSTR modeName;

		if (displayMode->GetName(&modeName) == S_OK)
		{
			ui->videoFormatPopup->addItem(QString::fromUtf16(reinterpret_cast<ushort*>(modeName)), QVariant::fromValue((uint64_t)displayMode->GetDisplayMode()));
			::SysFreeString(modeName);
		}
	});

	ui->videoFormatPopup->setCurrentIndex(0);
	enableStartButton(true);
}

void KeyerOutput::addDevice(com_ptr<IDeckLink>& deckLink)
{
	std::shared_ptr<DeckLinkKeyerDevice> outputDevice;
	try
	{
		outputDevice = std::make_shared<DeckLinkKeyerDevice>(this, deckLink);
	}
	catch (const std::runtime_error& e)
	{
		// Ignore devices that do not have both an output and keying interface.
		return;
	}

	// Get the duplex attribute for the device, filtering those that are inactive
	com_ptr<IDeckLinkProfileAttributes>		deckLinkAttributes(IID_IDeckLinkProfileAttributes, deckLink);
	int64_t									intAttribute;

	if (! deckLinkAttributes)
		return;

	if (deckLinkAttributes->GetInt(BMDDeckLinkDuplex, &intAttribute) != S_OK)
		return;

	// Store output device to map to maintain reference
	m_outputDevices[(intptr_t)deckLink.get()] = outputDevice;

	// Add this DeckLink device to the device list
	ui->outputDevicePopup->addItem(outputDevice->getDeviceName(), QVariant::fromValue<intptr_t>((intptr_t)deckLink.get()));

	// Set the enable state of the popup item based on whether DeckLink device is active
	int index = ui->outputDevicePopup->findData(QVariant::fromValue((intptr_t)deckLink.get()));
	if (index >= 0)
	{
		QStandardItemModel* model = qobject_cast<QStandardItemModel*>(ui->outputDevicePopup->model());
		QStandardItem* item = model->item(index);
		item->setEnabled(((BMDDuplexMode)intAttribute != bmdDuplexInactive));
	}

	if (ui->outputDevicePopup->count() == 1)
	{
		// We have added our first item, refresh and enable UI
		ui->outputDevicePopup->setCurrentIndex(0);
		outputDeviceChanged(0);

		enableInterface(true);
		ui->startButton->setText("Start");
	}
}

void KeyerOutput::removeDevice(com_ptr<IDeckLink>& deckLink)
{
	// If device to remove is selected device, stop playback if active
	if (m_selectedDevice->getDeckLinkInstance().get() == deckLink.get())
	{
		disable();
		m_selectedDevice = nullptr;
	}

	// Find the combo box entry to remove (there may be multiple entries with the same name, but each
	// will have a different data pointer).
	for (int i = 0; i < ui->outputDevicePopup->count(); ++i)
	{
		if (deckLink.get() == (IDeckLink*)((ui->outputDevicePopup->itemData(i)).value<void*>()))
		{
			ui->outputDevicePopup->removeItem(i);
			break;
		}
	}

	// Dereference output device by removing from list 
	auto iter = m_outputDevices.find((intptr_t)deckLink.get());
	if (iter != m_outputDevices.end())
	{
		m_outputDevices.erase(iter);
	}

	// Check how many devices are left
	if (ui->outputDevicePopup->count() == 0)
	{
		// We have removed the last device, disable the interface.
		enableInterface(false);
		enableStartButton(false);
	}
	else if (! m_selectedDevice)
	{
		// The device that was removed was the one selected in the UI.
		// Select the first available device in the list and reset the UI.
		ui->outputDevicePopup->setCurrentIndex(0);
		outputDeviceChanged(0);
	}
}

void KeyerOutput::playbackStopped()
{
	// Notify waiting process that scheduled playback has stopped
	{
		std::lock_guard<std::mutex> lock(m_mutex);
		m_scheduledPlaybackStopped = true;
	}
	m_stopPlaybackCondition.notify_one();
}

void KeyerOutput::enable()
{
	bool success = false;

	if (! m_selectedDevice)
		return;

	com_ptr<IDeckLinkOutput>			deckLinkOutput		= m_selectedDevice->getOutput();
	com_ptr<IDeckLinkKeyer>				deckLinkKeyer		= m_selectedDevice->getKeyer();
	com_ptr<IDeckLinkDisplayMode>		displayMode			= nullptr;

	BMDDisplayMode selectedDisplayMode = (BMDDisplayMode)ui->videoFormatPopup->currentData().value<uint64_t>();

	m_schedulerThread = new ScheduleFramesThread();
	connect(m_schedulerThread, &ScheduleFramesThread::scheduleNextFrame, this, &KeyerOutput::scheduleNextFrame);
	connect(m_schedulerThread, &ScheduleFramesThread::finished, m_schedulerThread, &QObject::deleteLater);

	// When a scheduled video frame is complete, schedule next frame
	m_selectedDevice->onScheduledFrameCompleted([this] { m_schedulerThread->scheduleFrameCompleted(); });

	// Register when scheduled playback stopped callback, to know when to disable video output
	m_selectedDevice->onScheduledPlaybackStopped([this] {
		playbackStopped();
	});

	if (deckLinkOutput->SetScreenPreviewCallback(m_previewView->delegate()) != S_OK)
		goto bail;

	// Get the IDeckLinkDisplayMode object associated with the selected display mode
	if (deckLinkOutput->GetDisplayMode(selectedDisplayMode, displayMode.releaseAndGetAddressOf()) != S_OK)
		goto bail;

	displayMode->GetFrameRate(&m_frameDuration, &m_frameTimescale);
	// Calculate the number of frames per second, rounded up to the nearest integer.  For example, for NTSC (29.97 FPS), framesPerSecond == 30.
	m_framesPerSecond = (m_frameTimescale + (m_frameDuration-1))  /  m_frameDuration;
	
	// Set the video preroll size and audio waterlevel to 1/2 second
	m_selectedDevice->setVideoPrerollSize(m_framesPerSecond / 2);

	// Set the video output mode
	if (deckLinkOutput->EnableVideoOutput(selectedDisplayMode, bmdVideoOutputFlagDefault) != S_OK)
		goto bail;
	
	// Enable Keyer and set to requested level
	if (deckLinkKeyer->Enable(ui->keyerExternalRadioButton->isChecked()) != S_OK)
		goto bail;

	deckLinkKeyer->SetLevel((uint8_t)ui->alphaBlendSlider->value());

	m_loopStills = ui->checkBox->isChecked();
	m_totalFramesScheduled = 0;

	// Start scheduler thread which will schedule preroll frames
	m_schedulerThread->start();
	
	// Success; update the UI
	m_outputEnabled = true;
	ui->startButton->setText("Stop");
	// Disable the user interface while running (prevent the user from making changes to the output signal)
	enableInterface(false);
	m_scheduledPlaybackStopped = false;

	success = true;
	
bail:
	if(! success)
	{
		QMessageBox::critical(this, "Failed to start output", "Failed to start output");
		disable();
	}
}

void KeyerOutput::disable()
{
	if (! m_outputEnabled)
		return;

	com_ptr<IDeckLinkOutput> deckLinkOutput = m_selectedDevice->getOutput();

	if (isRunning())
	{
		// Stop the audio and video output streams immediately
		deckLinkOutput->StopScheduledPlayback(0, nullptr, 0);

		// Wait until IDeckLinkVideoOutputCallback::ScheduledPlaybackHasStopped callback
		std::unique_lock<std::mutex> lock(m_mutex);
		m_stopPlaybackCondition.wait(lock, [this] { return m_scheduledPlaybackStopped; } );
	}

	// Deregister screen preview callback
	deckLinkOutput->SetScreenPreviewCallback(nullptr);

	deckLinkOutput->DisableAudioOutput();
	deckLinkOutput->DisableVideoOutput();
	
	m_selectedDevice->onScheduledFrameCompleted(nullptr);
	m_selectedDevice->onScheduledPlaybackStopped(nullptr);

	m_schedulerThread->exit();

	// Success; update the UI
	m_outputEnabled = false;
	ui->startButton->setText("Start");
	enableInterface(true);
}

bool KeyerOutput::isRunning()
{
	if (! m_selectedDevice)
		return false;

	BOOL playbackActive;
	if (m_selectedDevice->getOutput()->IsScheduledPlaybackRunning(&playbackActive) != S_OK)
		return false;

	return playbackActive;
}

void KeyerOutput::scheduleNextFrame()
{
	HRESULT									result = S_OK;
	com_ptr<IDeckLinkMutableVideoFrame>		outputFrame;
	com_ptr<IDeckLinkOutput>				deckLinkOutput = m_selectedDevice->getOutput();;

	if (! m_loopStills && m_totalFramesScheduled >= (unsigned)m_fileNames.size())
		return;

	QImage image(m_fileNames.at(m_totalFramesScheduled % m_fileNames.size()));
	com_ptr<ImageBuffer> imageBuffer = make_com_ptr<ImageBuffer>(image);

	int32_t frameWidth = image.width();
	int32_t frameHeight = image.height();
	int32_t frameRowBytes;
	deckLinkOutput->RowBytesForPixelFormat(kKeyerPixelFormat, frameWidth, &frameRowBytes);

	com_ptr<IDeckLinkVideoBuffer> videoBuffer(IID_IDeckLinkVideoBuffer, imageBuffer);
	result = deckLinkOutput->CreateVideoFrameWithBuffer(frameWidth, frameHeight, frameRowBytes, kKeyerPixelFormat, bmdFrameFlagDefault, videoBuffer.get(), outputFrame.releaseAndGetAddressOf());
	if (result != S_OK)
	{
		QMessageBox::critical(this, "CreateVideoFrameWithBuffer error", QString("Could not create video output frame - result = %1").arg((uint32_t)result, 8, 16));
		return;
	}

	result = deckLinkOutput->ScheduleVideoFrame(outputFrame.get(), (m_totalFramesScheduled * m_frameDuration), m_frameDuration, m_frameTimescale);
	if (result != S_OK)
	{
		QMessageBox::critical(this, "ScheduleVideoFrame error", QString("Could not schedule video output frame - result = %1").arg((uint32_t)result, 8, 16));
		return;
	}

	if (! isRunning())
	{
		uint32_t bufferedVideoFrameCount;
		if (deckLinkOutput->GetBufferedVideoFrameCount(&bufferedVideoFrameCount) != S_OK)
			return;

		// Start scheduled playback either when we hit preroll size, or run out of frame to schedule
		uint32_t prerollSize = kPrerollSize;
		if (! m_loopStills && (unsigned)m_fileNames.size() < kPrerollSize)
			prerollSize = m_fileNames.size();
		if (bufferedVideoFrameCount >= prerollSize)
		{
			result = deckLinkOutput->StartScheduledPlayback(0, m_frameTimescale, 1.0);
			if (result != S_OK)
			{
				QMessageBox::critical(this, "StartScheduledPlayback error", QString("Could not start scheduled playback - result = %1").arg((uint32_t)result, 8, 16));
				return;
			}
		}
	}

	++m_totalFramesScheduled;
}

void KeyerOutput::outputDeviceChanged(int selectedDeviceIndex)
{
	if (selectedDeviceIndex == -1)
		return;

	QVariant selectedDeviceVariant = ui->outputDevicePopup->itemData(selectedDeviceIndex);
	intptr_t deckLinkPtr = (intptr_t)selectedDeviceVariant.value<intptr_t>();

	// Find output device based on IDeckLink* object
	auto iter = m_outputDevices.find(deckLinkPtr);
	if (iter == m_outputDevices.end())
		return;

	m_selectedDevice = iter->second;

	// Update the video mode popup menu
	refreshDisplayModeMenu();

	ui->keyerInternalRadioButton->setEnabled(m_selectedDevice->doesSupportInternalKeying());
	ui->keyerExternalRadioButton->setEnabled(m_selectedDevice->doesSupportExternalKeying());
}

