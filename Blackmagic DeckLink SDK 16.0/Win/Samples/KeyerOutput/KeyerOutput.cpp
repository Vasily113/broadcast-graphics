ScheduleFramesThread::ScheduleFramesThread() :
	m_semaphore(kPrerollSize)
{
}

void ScheduleFramesThread::run()
{
	while (! isInterruptionRequested())
	{
		m_semaphore.acquire();
		emit scheduleNextFrame();
	}
}

void ScheduleFramesThread::scheduleFrameCompleted()
{
	m_semaphore.release();
}
