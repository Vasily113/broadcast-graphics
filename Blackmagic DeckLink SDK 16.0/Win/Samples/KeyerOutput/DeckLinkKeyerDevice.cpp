#include "com_utils.h"
#include "DeckLinkKeyerDevice.h"

// DeckLinkKeyerDeviceDelegate implementation

DeckLinkKeyerDeviceDelegate::DeckLinkKeyerDeviceDelegate() :
	m_refCount(1)
{
}

// IUnknown methods

HRESULT	DeckLinkKeyerDeviceDelegate::QueryInterface(REFIID iid, LPVOID *ppv)
{
	if (ppv == nullptr)
		return E_INVALIDARG;

	// Compare provided REFIID to IUnknown
	if (iid == IID_IUnknown)
	{
		*ppv = static_cast<IUnknown*>(this);
		AddRef();
	}
	else if (iid == IID_IDeckLinkVideoOutputCallback)
	{
		*ppv = static_cast<IDeckLinkVideoOutputCallback*>(this);
		AddRef();
	}
	else
	{
		*ppv = nullptr;
		return E_NOINTERFACE;
	}

	return S_OK;
}

ULONG DeckLinkKeyerDeviceDelegate::AddRef(void)
{
	return ++m_refCount;
}

ULONG DeckLinkKeyerDeviceDelegate::Release(void)
{
	ULONG newRefCount = --m_refCount;
	if (newRefCount == 0)
		delete this;

	return newRefCount;
}

/// IDeckLinkVideoOutputCallback methods

HRESULT DeckLinkKeyerDeviceDelegate::ScheduledFrameCompleted(IDeckLinkVideoFrame* completedFrame, BMDOutputFrameCompletionResult result)
{
	emit frameCompleted(com_ptr<IDeckLinkVideoFrame>(completedFrame), result);
	return S_OK;
}

HRESULT DeckLinkKeyerDeviceDelegate::ScheduledPlaybackHasStopped()
{
	emit playbackStopped();
	return S_OK;
}

// DeckLinkKeyerDevice implementation

DeckLinkKeyerDevice::DeckLinkKeyerDevice(QObject* owner, com_ptr<IDeckLink>& deckLink) :
	m_owner(owner),
	m_deckLink(deckLink),
	m_deckLinkOutput(IID_IDeckLinkOutput, deckLink),
	m_deckLinkKeyer(IID_IDeckLinkKeyer, deckLink)
{
	if (! m_deckLinkOutput)
	{
		// Device does not have IDeckLinkOutput interface, for example a DeckLink Mini Recorder
		throw std::runtime_error("A DeckLink output interface could not be queried.");
	}

	if (! m_deckLinkKeyer)
	{
		// Device does not have IDeckLinkKeyer interface, for example an Intensity Pro 4K
		throw std::runtime_error("A DeckLink keying interface could not be queried.");
	}

	BSTR deviceName;

	// Get device name
	if (m_deckLink->GetDisplayName(&deviceName) == S_OK)
	{
		m_deviceName = QString::fromUtf16(reinterpret_cast<ushort*>(deviceName));
		::SysFreeString(deviceName);
	}
	else
	{
		m_deviceName = QString("DeckLink");
	}

	com_ptr<IDeckLinkProfileAttributes> attributes(IID_IDeckLinkProfileAttributes, deckLink);

	if (attributes->GetFlag(BMDDeckLinkSupportsInternalKeying, &m_supportsInternalKeying) != S_OK)
		m_supportsInternalKeying = FALSE;

	if (attributes->GetFlag(BMDDeckLinkSupportsExternalKeying, &m_supportsExternalKeying) != S_OK)
		m_supportsExternalKeying = FALSE;

	m_delegate = make_com_ptr<DeckLinkKeyerDeviceDelegate>();
	connect(m_delegate.get(), &DeckLinkKeyerDeviceDelegate::frameCompleted, this, &DeckLinkKeyerDevice::frameCompleted, Qt::QueuedConnection);
	connect(m_delegate.get(), &DeckLinkKeyerDeviceDelegate::playbackStopped, this, &DeckLinkKeyerDevice::playbackStopped, Qt::DirectConnection);

	// Provide the delegate to the audio and video output interfaces
	m_deckLinkOutput->SetScheduledFrameCompletionCallback(m_delegate.get());
}

DeckLinkKeyerDevice::~DeckLinkKeyerDevice()
{
	m_deckLinkOutput->SetScheduledFrameCompletionCallback(nullptr);
}

void DeckLinkKeyerDevice::frameCompleted(com_ptr<IDeckLinkVideoFrame> /* completedFrame */, BMDOutputFrameCompletionResult result)
{
	if (m_scheduledFrameCompletedCallback && result != bmdOutputFrameFlushed)
		m_scheduledFrameCompletedCallback();
}

void DeckLinkKeyerDevice::playbackStopped()
{
	// Notify that playback has stopped, so it can disable output
	if (m_scheduledPlaybackStoppedCallback)
		m_scheduledPlaybackStoppedCallback();
}

/// Other methods

void DeckLinkKeyerDevice::queryDisplayModes(DisplayModeQueryFunc func)
{
	com_ptr<IDeckLinkDisplayModeIterator>	displayModeIterator;
	com_ptr<IDeckLinkDisplayMode>			displayMode;

	if (m_deckLinkOutput->GetDisplayModeIterator(displayModeIterator.releaseAndGetAddressOf()) != S_OK)
		return;

	while (displayModeIterator->Next(displayMode.releaseAndGetAddressOf()) == S_OK)
	{
		BOOL supported;
		if ((m_deckLinkOutput->DoesSupportVideoMode(bmdVideoConnectionUnspecified, displayMode->GetDisplayMode(),
													bmdFormatUnspecified, bmdNoVideoOutputConversion, 
													bmdSupportedVideoModeKeying, nullptr, &supported) == S_OK) && supported)
		{
			func(displayMode);
		}
	}
}
