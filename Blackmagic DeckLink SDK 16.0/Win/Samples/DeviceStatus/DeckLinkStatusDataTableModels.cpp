bool DeckLinkEthernetStatusDataTableModel::initInterfaces(com_ptr<IDeckLink>& deckLink)
{
	m_deckLinkStatus = com_ptr<IDeckLinkStatus>(IID_IDeckLinkStatus, deckLink);
	if (! m_deckLinkStatus)
		return false;
	
	m_deckLinkStatistics = com_ptr<IDeckLinkStatistics>(IID_IDeckLinkStatistics, deckLink);
	if (! m_deckLinkStatistics)
		return false;

	com_ptr<IDeckLinkProfileAttributes> deckLinkAttributes(IID_IDeckLinkProfileAttributes, deckLink);
	if (! deckLinkAttributes)
		return false;
	
	HRESULT result = deckLinkAttributes->GetInt(BMDDeckLinkNumberOfEthernetConnectors, &m_ethernetConnectorCount);
	if (result != S_OK)
		return false;
	
	return true;
}

bool DeckLinkEthernetStatusDataTableModel::paramIsValid(uint64_t param)
{
	return param == m_currentlySelectedConnector;
}

bool DeckLinkEthernetStatusDataTableModel::selectEthernetConnector(uint64_t newConnector)
{
	if (newConnector >= m_ethernetConnectorCount)
		return false;
	
	m_currentlySelectedConnector = newConnector;
	
	return true;
}

// Status data getter functions

QString getStatusFlag(com_ptr<IDeckLinkStatus>& deckLinkStatus, const BMDDeckLinkStatusID id)
{
	dlbool_t statusFlag;

	if (deckLinkStatus->GetFlag(id, &statusFlag) != S_OK)
		return QString();

	return statusFlag ? "Yes" : "No";
}

QString getStatusBytes(com_ptr<IDeckLinkStatus>& deckLinkStatus, const BMDDeckLinkStatusID id)
{
	uint32_t				bytesSize = 0;
	std::vector<uint8_t>	bytes;
	QStringList				bytesStringList;

	// Get required size of buffer
	if (deckLinkStatus->GetBytes(id, nullptr, &bytesSize) != S_OK)
		return QString();

	bytes.resize(bytesSize);

	if (deckLinkStatus->GetBytes(id, bytes.data(), &bytesSize) != S_OK)
		return QString();

	// Convert each byte to QStringList to display as hex
	for (auto byte : bytes)
		bytesStringList << QString("%1").arg(byte, 2, 16, QChar('0'));

	return bytesStringList.join(" ");
}

