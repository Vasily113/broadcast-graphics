QString getStatusFormatFlags(com_ptr<IDeckLinkStatus>& deckLinkStatus, const BMDDeckLinkStatusID id)
{
	int64_t statusInt;

	if (deckLinkStatus->GetInt(id, &statusInt) != S_OK)
		return QString();

	BMDFormatFlags formatFlags = (BMDFormatFlags)statusInt;

	QString description;

	if (formatFlags & bmdFormat8BitDepth)
		description.append("8-bit ");
	if (formatFlags & bmdFormat10BitDepth)
		description.append("10-bit ");
	if (formatFlags & bmdFormat12BitDepth)
		description.append("12-bit ");

	if (formatFlags & bmdFormatRGB444)
		description.append("RGB 4:4:4");
	if (formatFlags & bmdFormatYUV444)
		description.append("YUV 4:4:4");
	if (formatFlags & bmdFormatYUV422)
		description.append("YUV 4:2:2");
	if (formatFlags & bmdFormatYUV420)
		description.append("YUV 4:2:0");

	return description;
}

