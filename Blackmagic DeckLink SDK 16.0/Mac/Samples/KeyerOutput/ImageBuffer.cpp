#include "ImageBuffer.h"
#include "com_utils.h"

ImageBuffer::ImageBuffer(QImage& image) :
	m_refCount(1)
{
	m_image = image.convertToFormat(QImage::Format_ARGB32);
}

// IUnknown

HRESULT ImageBuffer::QueryInterface(REFIID iid, LPVOID *ppv)
{
	if (ppv == nullptr)
		return E_INVALIDARG;

	// Compare provided REFIID to IUnknown
	if (iid == IID_IUnknown)
	{
		*ppv = static_cast<IUnknown*>(this);
		AddRef();
	}
	else if (iid == IID_IDeckLinkVideoBuffer)
	{
		*ppv = static_cast<IDeckLinkVideoBuffer*>(this);
		AddRef();
	}
	else
	{
		*ppv = nullptr;
		return E_NOINTERFACE;
	}

	return S_OK;
}

ULONG ImageBuffer::AddRef()
{
	return ++m_refCount;
}

ULONG ImageBuffer::Release()
{
	ULONG newRefValue = --m_refCount;
	if (newRefValue == 0)
		delete this;

	return newRefValue;
}

// IDeckLinkVideoBuffer

HRESULT ImageBuffer::GetBytes(void** buffer)
{
	if (! buffer)
		return E_POINTER;

	const uchar* bits = m_image.bits();	// assign as const uchar to avoid deep copy

	*buffer = (void*)bits;
	return S_OK;
}

HRESULT ImageBuffer::GetSize(uint64_t* size)
{
	if (! size)
		return E_POINTER;

	*size = (uint64_t)m_image.sizeInBytes();
	return S_OK;
}

HRESULT ImageBuffer::StartAccess(BMDBufferAccessFlags /* flags */)
{
	return S_OK;
}

HRESULT ImageBuffer::EndAccess(BMDBufferAccessFlags /* flags */)
{
	return S_OK;
}
