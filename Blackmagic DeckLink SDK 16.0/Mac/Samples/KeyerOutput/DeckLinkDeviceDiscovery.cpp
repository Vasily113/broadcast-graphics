#include <QCoreApplication>
#include "com_utils.h"
#include "KeyerOutputEvents.h"
#include "DeckLinkDeviceDiscovery.h"


// DeckLinkDeviceDiscoveryDelegate implementation

DeckLinkDeviceDiscoveryDelegate::DeckLinkDeviceDiscoveryDelegate() :
	m_refCount(1)
{
}

// IUnknown methods

HRESULT DeckLinkDeviceDiscoveryDelegate::QueryInterface(REFIID iid, LPVOID* ppv)
{
	if (ppv == nullptr)
		return E_INVALIDARG;

	if (iid == IID_IUnknown)
	{
		*ppv = static_cast<IUnknown*>(this);
		AddRef();
	}
	else if (iid == IID_IDeckLinkDeviceNotificationCallback)
	{
		*ppv = static_cast<IDeckLinkDeviceNotificationCallback*>(this);
		AddRef();
	}
	else
	{
		*ppv = nullptr;
		return E_NOINTERFACE;
	}

	return S_OK;
}

ULONG DeckLinkDeviceDiscoveryDelegate::AddRef()
{
	return ++m_refCount;
}

ULONG DeckLinkDeviceDiscoveryDelegate::Release()
{
	ULONG newRefValue = --m_refCount;
	if (newRefValue == 0)
		delete this;

	return newRefValue;
}

// IDeckLinkDeviceArrivalNotificationCallback methods

HRESULT DeckLinkDeviceDiscoveryDelegate::DeckLinkDeviceArrived(IDeckLink *deckLinkDevice)
{
	emit deviceArrived(com_ptr<IDeckLink>(deckLinkDevice));
	return S_OK;
}

HRESULT DeckLinkDeviceDiscoveryDelegate::DeckLinkDeviceRemoved(IDeckLink *deckLinkDevice)
{
	emit deviceRemoved(com_ptr<IDeckLink>(deckLinkDevice));
	return S_OK;
}

// DeckLinkDeviceDiscovery implementation

DeckLinkDeviceDiscovery::DeckLinkDeviceDiscovery(QObject* owner) : 
	m_owner(owner)
{
	m_deckLinkDiscovery = CreateDeckLinkDiscoveryInstance();
	if (! m_deckLinkDiscovery)
	{
		throw std::runtime_error("A DeckLink discovery interface could not be created. The DeckLink drivers may not be installed.");
	}

	m_delegate = make_com_ptr<DeckLinkDeviceDiscoveryDelegate>();
	connect(m_delegate.get(), &DeckLinkDeviceDiscoveryDelegate::deviceArrived, this, &DeckLinkDeviceDiscovery::deviceArrived, Qt::QueuedConnection);
	connect(m_delegate.get(), &DeckLinkDeviceDiscoveryDelegate::deviceRemoved, this, &DeckLinkDeviceDiscovery::deviceRemoved, Qt::QueuedConnection);
}

DeckLinkDeviceDiscovery::~DeckLinkDeviceDiscovery()
{
	stop();
}

void DeckLinkDeviceDiscovery::deviceArrived(com_ptr<IDeckLink> deckLinkDevice)
{
	// Notify owner that device has been added
	QCoreApplication::postEvent(m_owner, new DeckLinkDeviceDiscoveryEvent(kAddDeviceEvent, deckLinkDevice));
}

void DeckLinkDeviceDiscovery::deviceRemoved(com_ptr<IDeckLink> deckLinkDevice)
{
	// Notify owner that device has been removed
	QCoreApplication::postEvent(m_owner, new DeckLinkDeviceDiscoveryEvent(kRemoveDeviceEvent, deckLinkDevice));
}

// Other methods

bool DeckLinkDeviceDiscovery::start()
{
	// Install device arrival notifications
	return m_deckLinkDiscovery->InstallDeviceNotifications(m_delegate.get()) == S_OK;
}

void DeckLinkDeviceDiscovery::stop()
{
	// Uninstall device arrival notifications
	m_deckLinkDiscovery->UninstallDeviceNotifications();
}
