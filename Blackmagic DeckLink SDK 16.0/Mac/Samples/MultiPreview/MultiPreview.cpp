MultiPreview::MultiPreview(int rows, int columns, QWidget *parent) :
	QDialog(parent),
	m_ui(new Ui::MultiPreview)
{
	setWindowFlags(Qt::Window
		| Qt::WindowMinimizeButtonHint
		| Qt::WindowMaximizeButtonHint
		| Qt::WindowCloseButtonHint);

	m_ui->setupUi(this);

	QSize defaultPreviewVideoSize = m_ui->previewContainer->size();
	QSize previewViewSize = defaultPreviewVideoSize;

	// The default preview grid size it optimized for 2x2, redraw dialog based on the requested preview windows.
	previewViewSize.setHeight(previewViewSize.height() * rows / 2);
	previewViewSize.setWidth(previewViewSize.width() * columns / 2);

	// Next, resize the preview if the size of the dialog goes outside of available screen size, retaining aspect ratio
	QScreen *screen = QGuiApplication::primaryScreen();
	QRect availableScreenGeometry = screen->availableGeometry();

	QSize dialogSize = this->window()->size();
	QSize dialogSizeWithoutPreview = dialogSize - defaultPreviewVideoSize;

	if ((dialogSizeWithoutPreview.width() + previewViewSize.width()) > availableScreenGeometry.width())
	{
		previewViewSize.setWidth(availableScreenGeometry.width() - dialogSizeWithoutPreview.width());
		previewViewSize.setHeight(previewViewSize.width() * (defaultPreviewVideoSize.height() * rows) / (defaultPreviewVideoSize.width() * columns) );
	}

	if ((dialogSizeWithoutPreview.height() + previewViewSize.height()) > availableScreenGeometry.height())
	{
		previewViewSize.setHeight(availableScreenGeometry.height() - dialogSizeWithoutPreview.height());
		previewViewSize.setWidth(previewViewSize.height() * (defaultPreviewVideoSize.width() * columns) / (defaultPreviewVideoSize.height() * rows));
	}

	m_ui->previewContainer->setMinimumSize(previewViewSize);

	/* Add a device control page for each preview to UI */
	for (int i = 0; i < rows * columns; ++i)
	{
		auto devicePage = new DeckLinkInputPage();
		devicePage->setSizePolicy(QSizePolicy::Policy::Preferred, QSizePolicy::Policy::Ignored);
		m_ui->toolBox->addItem(devicePage, QString("Input %1: None").arg(i + 1));
		m_devicePages.push_back(devicePage);
	}

	m_metalViewWidget = new DeckLinkMetalMultiViewWidget(this, m_devicePages.size());
	m_metalViewWidget->resize(previewViewSize);

	m_multiViewLayout = new QVBoxLayout(m_ui->previewContainer);
	m_multiViewLayout->setMargin(0);
	m_multiViewLayout->addWidget(m_metalViewWidget);

	m_previewLayout = new QGridLayout(m_metalViewWidget);
	m_previewLayout->setContentsMargins(0, 0, 0, 0);

	QSize previewSize = QSize(previewViewSize.width() / columns, previewViewSize.height() / rows);

	/* Add each preview to UI and connect signalling */
	for (size_t i = 0; i < m_devicePages.size(); i++)
	{
		m_devicePages[i]->setPreviewSize(previewSize);
		m_previewLayout->addWidget(m_devicePages[i]->getPreviewView(), /* row */ i / columns, /* column */ i % columns);

		connect(m_devicePages[i]->getPreviewView(), &DeckLinkPreviewWidget::setMultiViewFrame, [this, i] (com_ptr<IDeckLinkVideoFrame> frame) { m_metalViewWidget->setFrame(frame, i); });
		connect(m_devicePages[i]->getPreviewView(), &DeckLinkPreviewWidget::updateViewGeometry, [this, i] (QRect rect) { m_metalViewWidget->updateViewGeometry(rect, i); });
		connect(m_devicePages[i]->getPreviewView(), &DeckLinkPreviewWidget::paintOverlay, [this, i] { m_metalViewWidget->paintOverlay(i); });

		connect(m_devicePages[i], &DeckLinkInputPage::requestDeckLink, this, std::bind(&MultiPreview::requestDevice, this, m_devicePages[i], std::placeholders::_1));
		connect(m_devicePages[i], &DeckLinkInputPage::requestDeckLinkIfAvailable, std::bind(&MultiPreview::requestDeviceIfAvailable, this, m_devicePages[i], std::placeholders::_1));
		connect(m_devicePages[i], &DeckLinkInputPage::relinquishDeckLink, this, &MultiPreview::relinquishDevice);
	}

	connect(m_ui->deviceLabelCheckBox, &QCheckBox::stateChanged, this, &MultiPreview::deviceLabelEnableChanged);
	connect(m_ui->timecodeCheckBox, &QCheckBox::stateChanged, this, &MultiPreview::timecodeEnableChanged);

	this->resize(minimumSize());

	show();

	setup();
}

MultiPreview::~MultiPreview()
{
}

void MultiPreview::setup()
{
	// Create and initialise DeckLink device discovery and profile objects
	m_deckLinkDiscovery = make_com_ptr<DeckLinkDeviceDiscovery>(this);

	if (m_deckLinkDiscovery)
	{
		if (!m_deckLinkDiscovery->enable())
		{
			QMessageBox::critical(this, "This application requires the DeckLink drivers installed.", "Please install the Blackmagic DeckLink drivers to use the features of this application.");
		}
	}

	m_profileCallback = new ProfileCallback(this);
	m_profileCallback->onProfileChanging(std::bind(&MultiPreview::haltStreams, this, std::placeholders::_1));
}

void MultiPreview::customEvent(QEvent *event)
{
	switch (event->type())
	{
		case kAddDeviceEvent:
		{
			DeckLinkDeviceDiscoveryEvent* discoveryEvent = dynamic_cast<DeckLinkDeviceDiscoveryEvent*>(event);
			com_ptr<IDeckLink> deckLink(discoveryEvent->DeckLink());
			addDevice(deckLink);
		}
		break;

		case kRemoveDeviceEvent:
		{
			DeckLinkDeviceDiscoveryEvent* discoveryEvent = dynamic_cast<DeckLinkDeviceDiscoveryEvent*>(event);
			com_ptr<IDeckLink> deckLink(discoveryEvent->DeckLink());
			removeDevice(deckLink);
		}
		break;

		case kProfileActivatedEvent:
		{
			ProfileActivatedEvent* profileEvent = dynamic_cast<ProfileActivatedEvent*>(event);
			com_ptr<IDeckLinkProfile> deckLinkProfile(profileEvent->deckLinkProfile());
			updateProfile(deckLinkProfile);
		}
		break;

		case kErrorRestartingCaptureEvent:
		{

		}
		break;

		default:
		break;
	}
}

void MultiPreview::closeEvent(QCloseEvent *)
{
	for (auto& devicePage : m_devicePages)
	{
		com_ptr<DeckLinkInputDevice> selectedDevice = devicePage->getSelectedDevice();
		if (selectedDevice)
		{
			// Stop capture
			if (selectedDevice->isCapturing())
				selectedDevice->stopCapture();

			// Deregister profile callback
			com_ptr<IDeckLinkProfileManager> profileManager(IID_IDeckLinkProfileManager, selectedDevice->getDeckLinkInstance());
			if (profileManager)
				profileManager->SetCallback(nullptr);
		}
	}

	m_inputDevices.clear();

	if (m_deckLinkDiscovery)
		m_deckLinkDiscovery->disable();
}

void MultiPreview::addDevice(com_ptr<IDeckLink>& deckLink)
{
	com_ptr<IDeckLinkProfileAttributes>	deckLinkAttributes(IID_IDeckLinkProfileAttributes, deckLink);
	com_ptr<IDeckLinkProfileManager>	profileManager(IID_IDeckLinkProfileManager, deckLink);
	int64_t								videoIOSupport;

	// First check that device has an input interface
	if (!deckLinkAttributes)
		return;

	if ((deckLinkAttributes->GetInt(BMDDeckLinkVideoIOSupport, &videoIOSupport) != S_OK) ||
			((videoIOSupport & bmdDeviceSupportsCapture) == 0))
		// Device does not support capture, eg DeckLink Mini Monitor
		return;

	bool active = isDeviceActive(deckLink);

	m_inputDevices[deckLink] = (active ? DeviceState::Available : DeviceState::Inactive);

	for (auto& devicePage : m_devicePages)
		devicePage->addDevice(deckLink, active);

	// Get the profile manager interface
	// Will return S_OK when the device has > 1 profiles
	if (profileManager)
		profileManager->SetCallback(m_profileCallback);
}

void MultiPreview::removeDevice(com_ptr<IDeckLink>& deckLink)
{
	// Find input device to remove
	auto deviceIter = m_inputDevices.find(deckLink);
	if (deviceIter != m_inputDevices.end())
		deviceIter = m_inputDevices.erase(deviceIter);
	else
		return;

	// Check whether device to remove was selected device, if so then stop capture
	for (auto& devicePage : m_devicePages)
	{
		com_ptr<DeckLinkInputDevice> selectedDevice = devicePage->getSelectedDevice();
		if (selectedDevice == nullptr)
			continue;

		if (selectedDevice->getDeckLinkInstance().get() == deckLink.get())
		{
			selectedDevice->stopCapture();
			break;
		}
	}

	// Remove the device from each page
	for (auto& devicePage : m_devicePages)
		devicePage->removeDevice(deckLink);
}

void MultiPreview::haltStreams(com_ptr<IDeckLinkProfile> profile)
{
	com_ptr<IDeckLink> deviceToStop;

	// Profile is changing, stop capture if running
	if (profile->GetDevice(deviceToStop.releaseAndGetAddressOf()) != S_OK)
		return;

	for (auto& devicePage : m_devicePages)
	{
		com_ptr<DeckLinkInputDevice> selectedDevice = devicePage->getSelectedDevice();
		if (selectedDevice && (selectedDevice->getDeckLinkInstance() == deviceToStop) && selectedDevice->isCapturing())
		{
			selectedDevice->stopCapture();
			break;
		}
	}
}

void MultiPreview::updateProfile(com_ptr<IDeckLinkProfile>& newProfile)
{
	// Action as if new device selected to check whether device is active/inactive
	// This will subsequently update input connections and video modes combo boxes
	com_ptr<IDeckLink>	deckLink;
	bool				deviceActive;

	if (newProfile->GetDevice(deckLink.releaseAndGetAddressOf()) != S_OK)
		return;

	auto iter = m_inputDevices.find(deckLink);
	if (iter == m_inputDevices.end())
		return;

	deviceActive = isDeviceActive(deckLink);

	iter->second = deviceActive ? DeviceState::Available : DeviceState::Inactive;
	for (auto& devicePage : m_devicePages)
	{
		com_ptr<DeckLinkInputDevice> selectedDevice = devicePage->getSelectedDevice();
		if (selectedDevice && (selectedDevice->getDeckLinkInstance() == deckLink) && !selectedDevice->isCapturing())
		{
			// Restart capture if previously halted
			devicePage->startCapture();
		}

		devicePage->enableDevice(deckLink, deviceActive);
	}
}

bool MultiPreview::isDeviceAvailable(com_ptr<IDeckLink>& device)
{
	auto iter = m_inputDevices.find(device);
	if (iter == m_inputDevices.end())
		return false;

	return (iter->second != DeviceState::Inactive);
}

void MultiPreview::requestDevice(DeckLinkInputPage* page, com_ptr<IDeckLink>& deckLink)
{
	for (auto& devicePage : m_devicePages)
	{
		// Check each page (except itself) and stop/release device if already selected
		if (devicePage == page)
			continue;

		if (devicePage->releaseDeviceIfSelected(deckLink))
			break;
	}

	page->requestedDeviceGranted(deckLink);
}

void MultiPreview::requestDeviceIfAvailable(DeckLinkInputPage* page, com_ptr<IDeckLink>& deckLink)
{
	for (auto& devicePage : m_devicePages)
	{
		// Check each page (except itself) to see if device is already selected
		if (devicePage == page)
			continue;

		com_ptr<DeckLinkInputDevice> selectedDevice = devicePage->getSelectedDevice();
		if (selectedDevice && selectedDevice->getDeckLinkInstance() == deckLink)
			return;
	}

	page->requestedDeviceGranted(deckLink);
}

void MultiPreview::relinquishDevice(com_ptr<IDeckLink>& deckLink)
{
	auto iter = m_inputDevices.find(deckLink);
	if (iter == m_inputDevices.end())
		return;

	iter->second = DeviceState::Available;
}

void MultiPreview::deviceLabelEnableChanged(bool enabled)
{
	for (int i = 0; i < m_previewLayout->count(); ++i)
	{
#if __APPLE__
		DeckLinkPreviewWidget* widget = qobject_cast<DeckLinkPreviewWidget*>(m_previewLayout->itemAt(i)->widget());
#else
		DeckLinkOpenGLWidget* widget = qobject_cast<DeckLinkOpenGLWidget*>(m_previewLayout->itemAt(i)->widget());
#endif
		if (widget)
			widget->overlay()->enableDeviceLabel(enabled);
	}
}

void MultiPreview::timecodeEnableChanged(bool enabled)
{
	for (int i = 0; i < m_previewLayout->count(); ++i)
	{
#if __APPLE__
		DeckLinkPreviewWidget* widget = qobject_cast<DeckLinkPreviewWidget*>(m_previewLayout->itemAt(i)->widget());
#else
		DeckLinkOpenGLWidget* widget = qobject_cast<DeckLinkOpenGLWidget*>(m_previewLayout->itemAt(i)->widget());
#endif
		if (widget)
			widget->overlay()->enableTimecode(enabled);
	}
}
